export const pokemonDataId = (value: string) => value.toLowerCase().replace(/[^a-z0-9]+/g, "");

export function moveType(value: string): MoveType | null {
  const id = pokemonDataId(value);
  return (MOVE_TYPES[MOVE_ALIASES[id] ?? id] as MoveType | undefined) ?? null;
}

export function itemIconUrl(value: string | null): string | null {
  if (!value) return null;
  const slug = ITEM_SLUGS[pokemonDataId(value)];
  return slug ? `https://cdn.jsdelivr.net/gh/PokeAPI/sprites@e24fdb772a7568d06081645034fc03afb420ec1b/sprites/items/${slug}.png` : null;
}
